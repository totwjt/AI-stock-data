    def __init__(self, db_session: AsyncSession):
        self.db = db_session
    
    async def create_partition_tables(self, table_name: str, start_year: int, end_year: int):
        version_result = await self.db.execute(text("SELECT version()"))
        version = version_result.scalar()
        
        if "PostgreSQL 11" not in version and "PostgreSQL 12" not in version and "PostgreSQL 13" not in version and "PostgreSQL 14" not in version and "PostgreSQL 15" not in version and "PostgreSQL 16" not in version:
            raise Exception(f"当前 PostgreSQL 版本不支持分区表: {version}")
        
        await self._create_main_table(table_name)
        
        for year in range(start_year, end_year + 1):
            await self._create_partition_table(table_name, year)
        
        return True
    
