    async def _create_partition_table(self, table_name: str, year: int):
        """为指定年份创建分区表"""
        
        if table_name == "stock_factor_pro":
            partition_name = f"stock_factor_pro_{year}"
            start_date = f"{year}0101"
            end_date = f"{year + 1}0101"
            
            sql = f"""
            CREATE TABLE IF NOT EXISTS {partition_name} PARTITION OF stock_factor_pro
            FOR VALUES FROM ('{start_date}') TO ('{end_date}');
            """
            
            await self.db.execute(text(sql))
            await self.db.commit()
            
            index_sql = f"""
            CREATE INDEX IF NOT EXISTS idx_{partition_name}_ts_code ON {partition_name} (ts_code);
            CREATE INDEX IF NOT EXISTS idx_{partition_name}_trade_date ON {partition_name} (trade_date);
            """
            
            await self.db.execute(text(index_sql))
            await self.db.commit()